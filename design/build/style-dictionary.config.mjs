/*
 * design/build/style-dictionary.config.mjs
 *
 * Builds Snowdesk's design tokens from the DTCG JSON sources under
 * design/tokens/ into three target outputs:
 *
 *   1. static/css/tokens.css — Tailwind v4 @theme block (light) plus
 *      .dark { } overrides, concatenated. Imported by src/css/main.css.
 *   2. design/figma/tokens.json — Tokens Studio shape (separate
 *      light/dark sets) for round-trip Figma sync.
 *   3. design/docs/tokens.md — generated reference table for the team.
 *
 * Run via `npm run tokens`. Must run before the Tailwind build because
 * Tailwind reads the @theme block from the imported tokens.css to
 * generate utility classes.
 */

import StyleDictionary from 'style-dictionary';
import { fileURLToPath } from 'node:url';
import { dirname, join, basename, relative } from 'node:path';
import { mkdir, writeFile, readFile } from 'node:fs/promises';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..', '..');
const TOKENS_DIR = join(REPO_ROOT, 'design/tokens');
const TMP_DIR = join(REPO_ROOT, 'node_modules/.cache/style-dictionary-snow14');
const CSS_OUT = join(REPO_ROOT, 'static/css/tokens.css');
const FIGMA_OUT = join(REPO_ROOT, 'design/figma/tokens.json');
const DOCS_OUT = join(REPO_ROOT, 'design/docs/tokens.md');

/* ——————————————————————————————————————————————————————
   Custom format: themed CSS variables.
   Wraps allTokens in an arbitrary selector (`@theme`, `.dark`, …)
   so a single SD build can target either the light @theme block
   or a dark-mode override block. We avoid `css/variables` because
   it hard-codes `:root`.
   —————————————————————————————————————————————————————— */
const themedVariablesFormat = ({ dictionary, options }) => {
  const selector = options?.selector ?? ':root';
  const lines = dictionary.allTokens
    .map((token) => {
      const value = token.$value ?? token.value;
      return `  --${token.name}: ${value};`;
    })
    .join('\n');
  return `${selector} {\n${lines}\n}\n`;
};

/* ——————————————————————————————————————————————————————
   Build a Style Dictionary instance against a source glob,
   applying just the transforms we want — explicitly skipping
   `size/rem` so px values stay px (matches pre-migration CSS).
   —————————————————————————————————————————————————————— */
function makeSd({ source, destination, selector }) {
  return new StyleDictionary({
    source,
    hooks: {
      formats: { 'css/themed-variables': themedVariablesFormat },
    },
    platforms: {
      css: {
        transforms: ['attribute/cti', 'name/kebab', 'color/css', 'fontFamily/css'],
        buildPath: TMP_DIR + '/',
        files: [
          {
            destination,
            format: 'css/themed-variables',
            options: { selector },
          },
        ],
      },
    },
  });
}

/* ——————————————————————————————————————————————————————
   Token source files
   —————————————————————————————————————————————————————— */
const LIGHT_SOURCES = [
  join(TOKENS_DIR, 'colour.semantic.json'),
  join(TOKENS_DIR, 'colour.eaws.json'),
  join(TOKENS_DIR, 'typography.json'),
  join(TOKENS_DIR, 'radius.json'),
  join(TOKENS_DIR, 'breakpoints.json'),
];
const DARK_SOURCES = [
  join(TOKENS_DIR, 'colour.semantic.dark.json'),
];

/* ——————————————————————————————————————————————————————
   1. Build CSS — light + dark, then concatenate.
   —————————————————————————————————————————————————————— */
async function buildCss() {
  const sdLight = makeSd({
    source: LIGHT_SOURCES,
    destination: 'tokens-light.css',
    selector: '@theme',
  });
  const sdDark = makeSd({
    source: DARK_SOURCES,
    destination: 'tokens-dark.css',
    selector: '.dark',
  });

  await sdLight.buildAllPlatforms();
  await sdDark.buildAllPlatforms();

  const light = await readFile(join(TMP_DIR, 'tokens-light.css'), 'utf8');
  const dark = await readFile(join(TMP_DIR, 'tokens-dark.css'), 'utf8');

  const header = [
    '/*',
    ' * static/css/tokens.css — auto-generated by',
    ' * design/build/style-dictionary.config.mjs from design/tokens/*.json.',
    ' *',
    ' * DO NOT EDIT BY HAND. Run `npm run tokens` after editing any',
    ' * source JSON. This file is gitignored — committing it would',
    ' * mirror static/css/output.css (also gitignored as a build artefact).',
    ' */',
    '',
  ].join('\n');

  await mkdir(dirname(CSS_OUT), { recursive: true });
  await writeFile(CSS_OUT, header + '\n' + light + '\n' + dark);
}

/* ——————————————————————————————————————————————————————
   2. Build Figma JSON (Tokens Studio shape).
   Two sets — `light` and `dark` — wrapping the merged source
   tokens. Tokens Studio reads DTCG `$value`/`$type` natively
   and uses `$themes` / `$metadata.tokenSetOrder` for sets.
   —————————————————————————————————————————————————————— */
async function loadJson(path) {
  return JSON.parse(await readFile(path, 'utf8'));
}

async function buildFigma() {
  const lightSets = await Promise.all(LIGHT_SOURCES.map(loadJson));
  const darkSets = await Promise.all(DARK_SOURCES.map(loadJson));

  // Merge per-file objects into a single object per set. Keys conflict-free
  // because each source file owns a distinct top-level group (color, font,
  // radius, breakpoint, width, shadow).
  const merge = (sets) =>
    sets.reduce((acc, set) => {
      for (const [key, val] of Object.entries(set)) {
        if (key.startsWith('$')) continue; // strip per-file $description
        acc[key] = { ...(acc[key] ?? {}), ...val };
      }
      return acc;
    }, {});

  const figma = {
    $themes: [
      {
        id: 'light',
        name: 'Light',
        selectedTokenSets: { light: 'enabled' },
      },
      {
        id: 'dark',
        name: 'Dark',
        selectedTokenSets: { light: 'source', dark: 'enabled' },
      },
    ],
    $metadata: {
      tokenSetOrder: ['light', 'dark'],
    },
    light: merge(lightSets),
    dark: merge(darkSets),
  };

  await mkdir(dirname(FIGMA_OUT), { recursive: true });
  await writeFile(FIGMA_OUT, JSON.stringify(figma, null, 2) + '\n');
}

/* ——————————————————————————————————————————————————————
   3. Build Markdown reference.
   One section per source JSON file with a token table.
   —————————————————————————————————————————————————————— */
function flattenTokens(obj, prefix = []) {
  const out = [];
  for (const [key, val] of Object.entries(obj)) {
    if (key.startsWith('$')) continue;
    if (val && typeof val === 'object') {
      if ('$value' in val) {
        out.push({
          name: [...prefix, key].join('.'),
          value: val.$value,
          type: val.$type,
          description: val.$description ?? '',
        });
      } else {
        out.push(...flattenTokens(val, [...prefix, key]));
      }
    }
  }
  return out;
}

// File-level descriptions live here, not in the JSON — Style Dictionary
// reserves $-prefixed properties for token-graph metadata and chokes on
// any text that happens to contain `{ }`.
const FILE_DESCRIPTIONS = {
  'colour.semantic.json':
    'Light-mode semantic colour tokens. Source of truth for the @theme block in the compiled tokens.css.',
  'colour.semantic.dark.json':
    'Dark-mode overrides for the semantic colours. Emitted into a .dark selector — applied when the .dark class is on <html>. EAWS colours are intentionally absent (theme-invariant per spec).',
  'colour.eaws.json':
    'EAWS avalanche-danger colour scale. Exact specification colours — do not adjust for brand reasons. Theme-invariant.',
  'typography.json':
    'Font-family stacks. No size or weight tokens are defined — current Tailwind utilities and component CSS handle those directly.',
  'radius.json': 'Border-radius scale.',
  'breakpoints.json':
    'Layout dimensions: responsive breakpoints (consumed by Tailwind v4) and content max-widths.',
};

async function buildDocs() {
  const sections = [];
  const allFiles = [...LIGHT_SOURCES, ...DARK_SOURCES];
  for (const file of allFiles) {
    const json = await loadJson(file);
    const tokens = flattenTokens(json);
    const fileName = basename(file);
    const fileDescription = FILE_DESCRIPTIONS[fileName] ?? '';
    const rows = tokens
      .map((t) => `| \`${t.name}\` | \`${t.value}\` | ${t.type ?? ''} | ${t.description} |`)
      .join('\n');

    sections.push(
      `## \`${fileName}\`\n\n${fileDescription}\n\n` +
        `| Token | Value | Type | Description |\n` +
        `|-------|-------|------|-------------|\n` +
        `${rows}\n`,
    );
  }

  const body = [
    '# Snowdesk design tokens',
    '',
    '> Auto-generated by `design/build/style-dictionary.config.mjs`.',
    '> Edit the source JSON under `design/tokens/`, then run `npm run tokens`.',
    '',
    '## Why tokens live here',
    '',
    'Tokens are sourced from W3C DTCG JSON files so they round-trip cleanly between',
    'code and Figma (via the Tokens Studio plugin). Style Dictionary builds three',
    'consumers: the CSS bundle that Tailwind reads, this Markdown reference, and a',
    'Tokens Studio JSON at `design/figma/tokens.json`.',
    '',
    '## Figma bootstrap (one-time)',
    '',
    '1. Install Tokens Studio in Figma.',
    '2. Open the plugin → Settings → Token Storage → JSON, paste the contents of',
    '   `design/figma/tokens.json`.',
    '3. Save the imported tokens as a Figma Variables collection.',
    '4. Subsequent designer edits flow back via Tokens Studio export → repo PR.',
    '',
    '---',
    '',
    ...sections,
  ].join('\n');

  await mkdir(dirname(DOCS_OUT), { recursive: true });
  await writeFile(DOCS_OUT, body);
}

/* ——————————————————————————————————————————————————————
   Main
   —————————————————————————————————————————————————————— */
await mkdir(TMP_DIR, { recursive: true });
await buildCss();
await buildFigma();
await buildDocs();

console.log('Tokens built:');
console.log(`  ${relative(REPO_ROOT, CSS_OUT)}`);
console.log(`  ${relative(REPO_ROOT, FIGMA_OUT)}`);
console.log(`  ${relative(REPO_ROOT, DOCS_OUT)}`);
